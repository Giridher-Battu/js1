function add(a, b){
    let c=a+b;
    return c;// 11 give the sum of two nos
}
function sub(a, b){
    let c=a-b;
    return c;//1 difference b/w two nos
}
function mul(a, b){
    let c=a*b;
    return c;//30 multipication of two nos
}
function div(a, b){
    let c=a/b;// 2 give co-effecient of two nos
    return c;
}
function mod(a, b){
    let c=a%b;// 0 give the reminder
    return c;
}
function inc(a){
    a++;// 6 increase the value by 1
    return a;
}
function dic(b){
    b--;//  4 decrease the value by 1
    return b;
}
